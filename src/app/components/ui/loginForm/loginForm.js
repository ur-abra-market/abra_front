import React, { useEffect, useState } from "react";
import TextField from "../../commonComponents/textField/textField"
import { validator } from "../../../utils/validator";
import style from "../../commonComponents/buttons/buttons.module.css"

const LoginForm = () => {
    const [data, setData]=useState({email:"", password:""});
    const [errors, setErrors]=useState({})

    const handleChange = ({target}) => {
      setData((prevState) => ({
        ...prevState,
        [target.name]: target.value
      }));
    };

    const validatorConfig = {
        email:{ isRequired: {message: "Email is required!"}},
        password:{ isRequired: {message: "Password is required!"}},
    };
    useEffect(()=>{validate();},[data]);

    const validate = () => {
        const errors=validator(data, validatorConfig);
        setErrors(errors);
        return Object.keys(errors).length === 0
    };

    const handSubmit = (e) => {
        e.preventDefault();
        const isValid = validate();
        if (!isValid) return;
        console.log(data);
    };

    return (
        <form action="" onSubmit={handSubmit} >
            <TextField 
                label="Email" 
                name="email" 
                value={data.email} 
                onChange={handleChange}
                error={errors.email}

            />
            <TextField 
                label="Password" 
                type="password" 
                name="password" 
                value={data.password} 
                onChange={handleChange}
                error={errors.password}
            />
            <button className={style.mainButton}>Log in my WB account</button>
        </form>
    )
}
export default LoginForm;